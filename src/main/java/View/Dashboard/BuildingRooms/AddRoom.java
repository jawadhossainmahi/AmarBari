    private void setBuildingDropdownValue() {
        Users loggedInUser = LocalSession.user();
        if (loggedInUser == null) {
            System.out.println("No user logged in!");
            return;
        }

        ArrayList<Buildings> buildingses = Buildings.byUserId(loggedInUser.getId());
        jComboBox1.removeAllItems();

        // Add a dummy placeholder object
        Buildings placeholder = new Buildings();
        placeholder.setBuildingName("Please select");
        placeholder.setAddress("");
        placeholder.setPaymentDetails("");
        placeholder.setUserId(loggedInUser.getId());
        placeholder.setPolicy("");
// id = 0 means it's not a real building
// (you can also add a constructor for this case)
        jComboBox1.addItem(placeholder);

        for (Buildings bc : buildingses) {
            jComboBox1.addItem(bc);  // add the object itself
        }
        // Optionally force the combo box to show the placeholder initially
        jComboBox1.setSelectedIndex(0);

    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String room_name = jTextFieldRoomName.getText();
        String room_rent_text = jTextFieldRoomRent.getText();
        String room_details = jTextAreaRoomDetails.getText();
        Buildings building = (Buildings) jComboBox1.getSelectedItem();

        // Validation
        if (room_name == null || room_name.trim().isEmpty()
                || room_rent_text == null || room_rent_text.trim().isEmpty()
                || room_details == null || room_details.trim().isEmpty()
                || building == null || building.getId() == 0) {   // check placeholder
            JOptionPane.showMessageDialog(this,
                    "All fields are required!",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        // Check if rent is numeric
        Double room_rent;
        try {
            room_rent = Double.parseDouble(room_rent_text);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Room rent must be a valid number!",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        if (room_rent <= 0) {
            JOptionPane.showMessageDialog(this,
                    "Room rent must be greater than 0!",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        Rooms rooms = new Rooms(room_name, room_details, room_rent, building.getId());

        // ========== SAVE TO DATABASE ==========
        Response res = rooms.create();

        if (res.isSuccess()) {
            JOptionPane.showMessageDialog(this,
                    "Room created successfully!",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
            );

            // Clear fields
            jTextFieldRoomName.setText("");
            jTextFieldRoomRent.setText("");
            jTextAreaRoomDetails.setText("");
            jComboBox1.setSelectedIndex(0);

        } else {
            // Show SQL error to user
            JOptionPane.showMessageDialog(this,
                    "Failed to create Room!\n" + res.getMsg(),
                    "Database Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

