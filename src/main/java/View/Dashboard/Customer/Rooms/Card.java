    private void loadData() {
        if (room != null) {
            // Room name with icon
            jLabelRoomName.setText("üè† " + room.getRoomName());

            // Wrap details text so it auto-breaks
            jLabelRoomDetails.setText("<html><p style='width:250px'>" + room.getRoomDetails() + "</p></html>");

            // Rent with emoji
            jLabelRent.setText("üí≤ Rent: $" + room.getRent());
        }

        // ============================
        // Card Styling
        // ============================
        setBorder(javax.swing.BorderFactory.createCompoundBorder(
                javax.swing.BorderFactory.createLineBorder(new java.awt.Color(200, 200, 200), 1),
                javax.swing.BorderFactory.createEmptyBorder(15, 15, 15, 15) // padding
        ));
        setBackground(new java.awt.Color(245, 245, 245));

        // Fonts
        jLabelRoomName.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 16));
        jLabelRoomDetails.setFont(new java.awt.Font("Arial", java.awt.Font.PLAIN, 13));
        jLabelRent.setFont(new java.awt.Font("Arial", java.awt.Font.ITALIC, 14));

        // Book button styling
        jButtonBook.setText("Details");
        jButtonBook.setBackground(new java.awt.Color(0, 153, 255));
        jButtonBook.setForeground(java.awt.Color.WHITE);
        jButtonBook.setFocusPainted(false);
        jButtonBook.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 12));

        // Hover effect for button
        jButtonBook.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseEntered(java.awt.event.MouseEvent evt) {
                jButtonBook.setBackground(new java.awt.Color(0, 120, 200)); // darker blue
            }

            @Override
            public void mouseExited(java.awt.event.MouseEvent evt) {
                jButtonBook.setBackground(new java.awt.Color(0, 153, 255)); // original blue
            }
        });
    }


